const inferMaturity = (kind: EntityKind, confidence: ResearchConfidence): TermMaturity => {
  if (kind === "umbrella") return "broad";
  if (kind === "misnomer" || kind === "release") return "disputed";
  if (confidence === "low") return "unconfirmed";
  if (kind === "genre" || kind === "microgenre") return confidence === "high" ? "confirmed" : "local";
  return "local";
};

