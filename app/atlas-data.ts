export const getLineage = (entry: AtlasEntry) => {
  const lineage: AtlasEntry[] = [entry];
  let current = entry;
  const seen = new Set([entry.id]);
  while (current.parent && entryById.has(current.parent) && !seen.has(current.parent)) {
    current = entryById.get(current.parent)!;
    lineage.unshift(current);
    seen.add(current.id);
  }
  return lineage;
};

export const transliterate = (value: string) => {
  const table: Record<string, string> = {
    а: "a", б: "b", в: "v", г: "g", д: "d", е: "e", ё: "e", ж: "zh", з: "z", и: "i", й: "y",
    к: "k", л: "l", м: "m", н: "n", о: "o", п: "p", р: "r", с: "s", т: "t", у: "u", ф: "f",
    х: "h", ц: "ts", ч: "ch", ш: "sh", щ: "sch", ъ: "", ы: "y", ь: "", э: "e", ю: "yu", я: "ya",
  };
  return value.toLowerCase().split("").map((character) => table[character] ?? character).join("");
};

export const normalizeSearch = (value: string) =>
  transliterate(value)
    .replace(/[^a-z0-9]+/g, " ")
    .trim();

const subsequenceScore = (query: string, target: string) => {
  if (!query) return 0;
  if (target === query) return 120;
  if (target.startsWith(query)) return 100 - Math.min(20, target.length - query.length);
  if (target.includes(query)) return 80 - Math.min(20, target.indexOf(query));
  let cursor = 0;
  for (const character of target) if (character === query[cursor]) cursor += 1;
  return cursor === query.length ? 35 - Math.min(20, target.length - query.length) : 0;
};

export const searchEntries = (query: string) => {
  const normalized = normalizeSearch(query);
  if (!normalized) return [];
  const tokens = normalized.split(" ");
  return entries
    .map((entry) => {
      const primary = normalizeSearch(entry.name);
      const aliases = entry.aliases.map(normalizeSearch);
      const searchable = normalizeSearch(
        [
          entry.name,
          ...entry.aliases,
          ...entry.artists,
          ...entry.producers,
          ...entry.examples.flatMap((example) => [example.artist, example.title, example.listenFor]),
          entry.summary,
          entry.signature,
          entry.verdict,
          ...entry.listenFor,
          ...entry.production,
          ...entry.tags,
        ].join(" "),
      );
      const nameScore = Math.max(subsequenceScore(normalized, primary), ...aliases.map((alias) => subsequenceScore(normalized, alias)));
      const tokenScore = tokens.reduce((score, token) => score + (searchable.includes(token) ? 14 : 0), 0);
      return { entry, score: nameScore + tokenScore };
    })
    .filter((result) => result.score > 0)
    .sort((a, b) => b.score - a.score || a.entry.name.localeCompare(b.entry.name))
    .slice(0, 18);
};

